#include "PrimitiveFactory.h"

Model PrimitiveFactory::CreateCube()
{
    float vertices[] = {
        -0.5, -0.5,  0.5,   0.0, 0.0, // 0
         0.5, -0.5,  0.5,   1.0, 0.0, // 1
         0.5,  0.5,  0.5,   1.0, 1.0, // 2
        -0.5,  0.5,  0.5,   0.0, 1.0, // 3

        // Back face
        -0.5, -0.5, -0.5,   1.0, 0.0, // 4
        -0.5,  0.5, -0.5,   1.0, 1.0, // 5
         0.5,  0.5, -0.5,   0.0, 1.0, // 6
         0.5, -0.5, -0.5,   0.0, 0.0, // 7

        // Top face
        -0.5,  0.5, -0.5,   0.0, 1.0, // 8
        -0.5,  0.5,  0.5,   0.0, 0.0, // 9
         0.5,  0.5,  0.5,   1.0, 0.0, // 10
         0.5,  0.5, -0.5,   1.0, 1.0, // 11

        // Bottom face
        -0.5, -0.5, -0.5,   1.0, 1.0, // 12
         0.5, -0.5, -0.5,   0.0, 1.0, // 13
         0.5, -0.5,  0.5,   0.0, 0.0, // 14
        -0.5, -0.5,  0.5,   1.0, 0.0, // 15

        // Right face
         0.5, -0.5, -0.5,   1.0, 0.0, // 16
         0.5,  0.5, -0.5,   1.0, 1.0, // 17
         0.5,  0.5,  0.5,   0.0, 1.0, // 18
         0.5, -0.5,  0.5,   0.0, 0.0, // 19

        // Left face
        -0.5, -0.5, -0.5,   0.0, 0.0, // 20
        -0.5, -0.5,  0.5,   1.0, 0.0, // 21
        -0.5,  0.5,  0.5,   1.0, 1.0, // 22
        -0.5,  0.5, -0.5,   0.0, 1.0
    };

    uint32_t indices[] =
    {
        0,  1,  2,      0,  2,  3,    // front
        4,  5,  6,      4,  6,  7,    // back
        8,  9,  10,     8,  10, 11,   // top
        12, 13, 14,     12, 14, 15,   // bottom
        16, 17, 18,     16, 18, 19,   // right
        20, 21, 22,     20, 22, 23    // left
    };

    const auto mesh = Mesh(vertices, 120, indices, 36);
    auto model = Model(mesh);

    return model;
}
